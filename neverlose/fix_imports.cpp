void neverlose::fix_imports()
{
    auto logger = ENTER_LOGGER(logman);

    logger << "Resolving " << (int)(sizeof(g_imports) / sizeof(g_imports[0])) << " imports...\n";
    //logger << "Resolving " << (int)g_imports.size() << " imports...\n";

    for (const auto& imp : g_imports)
    {
        HMODULE hMod = GetModuleHandleW(imp.module_name);
        if (!hMod)
        {
            // Пытаемся загрузить, если модуль еще не в памяти
            hMod = LoadLibraryW(imp.module_name);
        }

        if (!hMod)
            panic("Failed to resolve module: %ls", imp.module_name);

        FARPROC proc_addr = NULL;
        if (imp.is_ordinal)
        {
            proc_addr = GetProcAddress(hMod, (LPCSTR)LOWORD(imp.procordinal));
        }
        else
        {
            proc_addr = GetProcAddress(hMod, imp.procname);
        }

        if (!proc_addr)
            panic("Failed to find function in %ls", imp.module_name);

        // Записываем адрес функции по целевому адресу в памяти чита
        *(uintptr_t*)imp.address = (uintptr_t)proc_addr;

        // Явное приведение для логгера (C2297 fix)
        logger << "Imported " << (uintptr_t)imp.address << " -> " << (uintptr_t)proc_addr << "\n";
    }
}